package cn._2dland.lab.swf.struct;

public class Rectangle {

	private int bitCount;
	private int xMin;
	private int xMax;
	private int yMin;
	private int yMax;

	public int getBitCount() {
		return bitCount;
	}
	public void setBitCount(int bitCount) {
		this.bitCount = bitCount;
	}
	public int getxMin() {
		return xMin;
	}
	public void setxMin(int xMin) {
		this.xMin = xMin;
	}
	public int getxMax() {
		return xMax;
	}
	public void setxMax(int xMax) {
		this.xMax = xMax;
	}
	public int getyMin() {
		return yMin;
	}
	public void setyMin(int yMin) {
		this.yMin = yMin;
	}
	public int getyMax() {
		return yMax;
	}
	public void setyMax(int yMax) {
		this.yMax = yMax;
	}

}
